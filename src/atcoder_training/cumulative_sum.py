"""累積和 / いもす法。

■ 累積和とは（たとえ話）
  「ここまでの合計」を最初に全部メモしておくと、
  区間[l, r]の合計が「r までの合計 − l-1 までの合計」の引き算1回で出る。
  毎回足し直す O(N) が、前計算しておけば1クエリ O(1) になる魔法。

■ いもす法とは
  「区間に +1 する」操作が何回も来るとき、毎回塗ると遅い。
  区間の始まりに +1、終わりの次に -1 だけ記録し、最後に累積和を取ると
  全区間の重なり回数が一気に求まる。イベントの「入り/出」だけ記録する発想。

実行:
    python -m atcoder_training.cumulative_sum
"""

from __future__ import annotations

from itertools import accumulate

from .common import run_cases


# ─────────────────────────────────────────────────────────
# 問題1: 区間和クエリ（1次元累積和）
# ─────────────────────────────────────────────────────────
def range_sums(a: list[int], queries: list[tuple[int, int]]) -> list[int]:
    """問題: 配列 a に対し、各クエリ (l, r)（0-index, 両端含む）の区間和を答える。

    制約: 1 ≤ N, Q ≤ 10^5（毎回足すと O(NQ)=遅い → 累積和で O(N+Q)）
    入力例: a=[1,2,3,4], queries=[(0,2),(1,3)] → [6, 9]

    考え方: prefix[i] = a[0]+…+a[i-1]（prefix[0]=0）。
            区間和[l,r] = prefix[r+1] - prefix[l]。
    計算量: 前計算 O(N) + 各クエリ O(1)
    """
    prefix = [0] + list(accumulate(a))      # prefix[i] = a[:i] の和
    return [prefix[r + 1] - prefix[l] for (l, r) in queries]


# ─────────────────────────────────────────────────────────
# 問題2: いもす法（区間加算の重なり）
# ─────────────────────────────────────────────────────────
def imos_overlaps(n: int, intervals: list[tuple[int, int]]) -> list[int]:
    """問題: 長さ n の数直線(マス 0..n-1)に、各区間 [l, r]（両端含む）で +1 する。
    最終的な各マスの値（＝重なり回数）を返す。

    制約: 1 ≤ n, 区間数 ≤ 10^5
    入力例: n=5, intervals=[(0,2),(1,3)] → [1,2,2,1,0]

    考え方(いもす法): 区間ごとに diff[l]+=1, diff[r+1]-=1 だけ記録 →
                     最後に累積和を取ると各マスの重なり回数になる。
    計算量: O(n + 区間数)
    """
    diff = [0] * (n + 1)
    for l, r in intervals:
        diff[l] += 1
        diff[r + 1] -= 1                    # r の「次」で打ち消す
    result = list(accumulate(diff))[:n]     # 累積和を取り、長さ n に切る
    return result


# ─────────────────────────────────────────────────────────
# 問題3: 2次元累積和（長方形領域の和）
# ─────────────────────────────────────────────────────────
def rect_sum(grid: list[list[int]], r1: int, c1: int, r2: int, c2: int) -> int:
    """問題: 2次元グリッドで、左上(r1,c1)〜右下(r2,c2)（両端含む）の長方形の和。

    入力例: grid=[[1,2],[3,4]], (0,0)-(1,1) → 10
    入力例: grid=[[1,2,3],[4,5,6]], (0,1)-(1,2) → 16（2+3+5+6）

    考え方: 2次元累積和 S を作り、包除原理で
            和 = S[r2+1][c2+1] - S[r1][c2+1] - S[r2+1][c1] + S[r1][c1]。
    計算量: 前計算 O(HW) + 各クエリ O(1)
    """
    h, w = len(grid), len(grid[0])
    S = [[0] * (w + 1) for _ in range(h + 1)]
    for i in range(h):
        for j in range(w):
            S[i + 1][j + 1] = grid[i][j] + S[i][j + 1] + S[i + 1][j] - S[i][j]
    return S[r2 + 1][c2 + 1] - S[r1][c2 + 1] - S[r2 + 1][c1] + S[r1][c1]


def main() -> None:
    run_cases("問題1 区間和クエリ(累積和)", [
        ("a=[1,2,3,4]", lambda: range_sums([1, 2, 3, 4], [(0, 2), (1, 3)]), [6, 9]),
    ])
    run_cases("問題2 いもす法(重なり)", [
        ("n=5,[(0,2),(1,3)]", lambda: imos_overlaps(5, [(0, 2), (1, 3)]), [1, 2, 2, 1, 0]),
    ])
    run_cases("問題3 2次元累積和(長方形和)", [
        ("2x2 全体", lambda: rect_sum([[1, 2], [3, 4]], 0, 0, 1, 1), 10),
        ("2x3 部分", lambda: rect_sum([[1, 2, 3], [4, 5, 6]], 0, 1, 1, 2), 16),
    ])


if __name__ == "__main__":
    main()
